#include "Player.h"
#include "Plaza.h"

void Player::make()
{
	cout << "캐릭터의 이름을 입력하세요 : ";
	cin >> _name;
	
	int jobNum = 0;
	do
	{
		cout << "\n현재 가능한 직업의 목록\n";
		cout << "1. 전사\n";
		cout << "2. 마법사\n";
		cout << "\n캐릭터의 직업을 골라주세요 : ";
		cin >> jobNum;
	} while (jobNum != 1 && jobNum != 2);

	if (jobNum == 1)
	{
		_job = "전사";
		_str = 5;
		_dmg = _str * 2;
	}
	else 
	{ 
		_job = "마법사"; 
		_intel = 5;
		_dmg = _intel * 2;
	}

	cout << "\n캐릭터 생성이 완료되었습니다.\n";
	system("cls");

	Plaza p;
	
	p.update();
}

void Player::showInfo() const
{
	cout << "캐릭터 이름 : " << _name << "|| Lv : " << _level << endl;
}